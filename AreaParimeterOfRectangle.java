class AreaParimeterOfRectangle{
	public static void main(String[] args) {
		double hieght=8.5;
		double wedth=5.6;

		double area=wedth*hieght;
		double perimeter=2*(wedth + hieght);
		System.out.println(" area is "+area);
		System.out.println("perimeter is "+perimeter);
	}
}

