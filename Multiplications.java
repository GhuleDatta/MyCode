import java.util.*;
class Multiplications{
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.print("Enter the num1:");
		int num1=sc.nextInt();
		System.out.print("Enter the Num2:");
		int num2=sc.nextInt();
		int c=num1*num2;
		System.out.println(num1+"*"+num2+"="+c);
	}
}

